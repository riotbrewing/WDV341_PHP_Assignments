class Armor
{
    constructor(type, name, cost, armor_class, modifier, modifier_max, strength, stealth, weight) {
        this.type = type;
        this.name = name;
        this.cost = cost;
        this.armor_class = armor_class;
        this.modifier = modifier;
        this.modifier_max = modifier_max;
        this.strength = strength;
        this.stealth = stealth;
        this.weight = weight;
    }
}